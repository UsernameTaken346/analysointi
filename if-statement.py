number1 = 5
number2 = 7


if number1 == number2:
    print("number1 and number2 are equal")


if number1 > number2:
    print("number1 is greater than number2")


if number1 >= number2:
    print("number1 is greater or equal to number2")


if number1 != number2:
    print("number1 and number2 are not equal")